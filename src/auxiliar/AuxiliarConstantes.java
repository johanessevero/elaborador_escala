	public static int getTerminoSemana(int mes, int semana, int ano) {
		
		int contSemanas = 0;
		int terminoSemana = 0;
		for (int i = 0; i < AuxiliarConstantes.getUltimoDiaMes(mes, ano); ++i) {
			
			if (AuxiliarConstantes.getDiaSemanaData(i + 1, mes, ano) == AuxiliarConstantes.SABADO || (i + 1) ==  AuxiliarConstantes.getUltimoDiaMes(mes, ano)) {
				
				contSemanas += 1;
				
				if (contSemanas == semana) {
					
					terminoSemana = i;
					
				}
				
			}
		}
		
		return terminoSemana;
		
	}
	
	public static String getDiaSemanaData(int dia, int mes, int ano) {
		
		Calendar cal = new GregorianCalendar(ano, mes - 1, dia);
		//Calendar cal = new GregorianCalendar(2010, mes - 1, dia);

		String diaSemanaStr = "";
		int diaSemanaInt = cal.get(Calendar.DAY_OF_WEEK);
		
		switch (diaSemanaInt) {
		
		case Calendar.SUNDAY:
			diaSemanaStr = AuxiliarConstantes.DOMINGO;
			break;
		case Calendar.MONDAY:
			diaSemanaStr = AuxiliarConstantes.SEGUNDA;
			break;
		case Calendar.TUESDAY:
			diaSemanaStr = AuxiliarConstantes.TERCA;
			break;
		case Calendar.WEDNESDAY:
			diaSemanaStr = AuxiliarConstantes.QUARTA;
			break;
		case Calendar.THURSDAY:
			diaSemanaStr = AuxiliarConstantes.QUINTA;
			break;
		case Calendar.FRIDAY:
			diaSemanaStr = AuxiliarConstantes.SEXTA;
			break;
		case Calendar.SATURDAY:
			diaSemanaStr = AuxiliarConstantes.SABADO;
			break;			
		}
		
		return diaSemanaStr;
	}
	
